use Yarok_MyBASE;

select Товары.Наименование, Заказы.Дата_поставки,
	Case
	when (Заказы.Цена_продажи between 1 and 1500) then 'Цена < 1500'
	when (Заказы.Цена_продажи between 1500 and 3000) then 'Цена от 1500 до 3000'
	else 'Цена больше 3000'
	end [Пределы цен]
from Товары inner join Заказы
on Товары.Наименование = Заказы.Наименование_заказа
	Order by Заказы.Наименование_заказа